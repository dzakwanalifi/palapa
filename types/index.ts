// Route Optimization API
export interface RouteOptimizationRequest {
  destinations: { lat: number; lng: number; id: string }[];
  startPoint?: { lat: number; lng: number };
}

export interface RouteOptimizationResponse {
  optimizedRoute: OptimizedDestination[];
  totalDistance: number;
  estimatedTime: number;
}

export interface OptimizedDestination {
  id: string;
  name: string;
  lat: number;
  lng: number;
  order: number;
  distanceToNext?: number;
  timeToNext?: number;
}

// OSRM Route Types
export interface OptimizedRoute {
  waypoints: RouteWaypoint[];
  route: {
    geometry: {
      coordinates: [number, number][];
      type: 'LineString';
    };
    legs: RouteLeg[];
    distance: number;
    duration: number;
  };
  totalDistance: number;
  estimatedTime: number;
  instructions?: RouteInstruction[];
}

export interface RouteWaypoint {
  location: {
    lat: number;
    lng: number;
  };
  name?: string;
  distance?: number; // Distance to next waypoint in meters
  duration?: number; // Duration to next waypoint in seconds
}

export interface RouteLeg {
  distance: number;
  duration: number;
  steps: RouteStep[];
}

export interface RouteStep {
  distance: number;
  duration: number;
  geometry: {
    coordinates: [number, number][];
  };
  maneuver: {
    type: string;
    modifier?: string;
  };
  name?: string;
  mode: string;
  instruction: string;
}

export interface RouteInstruction {
  text: string;
  distance?: number;
  time?: number;
  type?: string;
}

