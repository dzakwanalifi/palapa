// PALAPA - TypeScript Type Definitions
// Based on Firestore Collections Schema

import { Timestamp } from 'firebase/firestore';

// Base types
export type PriceRange = 'murah' | 'sedang' | 'mahal';
export type Category = 'alam' | 'budaya' | 'kuliner' | 'religius' | 'petualangan' | 'belanja';
export type Province = 'aceh' | 'sumatera-utara' | 'sumatera-barat' | 'riau' | 'jambi' | 'sumatera-selatan' |
  'bengkulu' | 'lampung' | 'kepulauan-bangka-belitung' | 'kepulauan-riau' | 'dki-jakarta' |
  'jawa-barat' | 'jawa-tengah' | 'di-yogyakarta' | 'jawa-timur' | 'banten' | 'bali' |
  'nusa-tenggara-barat' | 'nusa-tenggara-timur' | 'kalimantan-barat' | 'kalimantan-tengah' |
  'kalimantan-selatan' | 'kalimantan-timur' | 'kalimantan-utara' | 'sulawesi-utara' |
  'sulawesi-tengah' | 'sulawesi-selatan' | 'sulawesi-tenggara' | 'gorontalo' | 'sulawesi-barat' |
  'maluku' | 'maluku-utara' | 'papua-barat' | 'papua';

export type TransportMode = 'mobil_pribadi' | 'motor' | 'bus' | 'kereta' | 'pesawat' | 'kapal' | 'ojek' | 'taksi' | 'jalan_kaki';

export interface DestinationFacilities {
  wifi?: boolean;
  toilet?: boolean;
  parking?: boolean;
  accessibility?: boolean; // For disabled visitors
  restaurant?: boolean;
  prayer_room?: boolean;
  locker?: boolean;
  guide_service?: boolean;
  audio_guide?: boolean;
  shop?: boolean;
}

export interface TicketPricing {
  adult?: number; // Price for adults in IDR
  child?: number; // Price for children in IDR
  senior?: number; // Price for seniors in IDR
  foreign_adult?: number; // Price for foreign adults in IDR
  foreign_child?: number; // Price for foreign children in IDR
  currency?: string; // Default: 'IDR'
  last_updated?: Timestamp;
  notes?: string; // Additional pricing notes
}

// Firestore Document Base
export interface FirestoreDoc {
  id?: string; // Auto-generated by Firestore
  createdAt: Timestamp | { seconds: number; nanoseconds: number };
  updatedAt: Timestamp | { seconds: number; nanoseconds: number };
}

// Destinations Collection
export interface Destination extends FirestoreDoc {
  name: string;
  category: Category;
  latitude: number;
  longitude: number;
  address: string;
  description: string;
  descriptionClean: string;
  priceRange: PriceRange;
  rating: number;
  timeMinutes: number; // Estimated visit time in minutes
  imageUrl: string;
  imagePath: string;
  provinsi: Province;
  kotaKabupaten: string;
  isCultural: boolean;
  umkmId?: string | null; // Reference to UMKM if available
  facilities?: DestinationFacilities; // Facility details
  transport_modes?: TransportMode[]; // Available transport options
  best_visit_times?: string[]; // Best times to visit
  ticket_pricing?: TicketPricing; // Specific ticket prices
}

// UMKM Collection
export interface UMKM extends FirestoreDoc {
  name: string;
  category: 'batik' | 'kuliner' | 'kerajinan';
  latitude: number;
  longitude: number;
  address: string;
  phone: string;
  whatsapp: string;
  verified: boolean;
}

// Users Collection (extends Firebase Auth)
export interface UserProfile extends FirestoreDoc {
  uid: string; // From Firebase Auth
  budgetRange?: PriceRange | null;
  preferredCategories?: Category[];
}

// Itineraries Collection
export interface Itinerary extends FirestoreDoc {
  userId: string;
  destinations: string[]; // Array of destination IDs
  totalBudget: number;
  durationDays: number;
  geminiResponse: any; // Structured JSON from Gemini API
}

// Local Guides Collection
export interface LocalGuide extends FirestoreDoc {
  name: string;
  location: string;
  languages: string[];
  specialties: string[]; // e.g., 'history', 'food', 'nature'
  rating: number;
  pricePerDay: number;
  imageUrl: string;
  contact: string;
  verified: boolean;
}

// API Request/Response Types

